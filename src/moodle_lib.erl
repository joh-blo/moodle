-module(moodle_lib).

%% API
-export([read_cfg/0,
	 get_cfg/1,get_cfg/2]).

-define(APP_NAME,moodle).

%%%===================================================================
%%% API
%%%===================================================================
read_cfg() ->
    emd_cfg:read_cfg(?APP_NAME).

get_cfg(Cfg) ->
    emd_cfg:get_cfg_a(?APP_NAME,Cfg).

get_cfg(Cfg,Def) ->
    emd_cfg:get_cfg_a(?APP_NAME,Cfg,Def).

